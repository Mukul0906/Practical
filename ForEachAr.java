package ControlStatement;

public class ForEachAr {
	public static void main(String[] args) {
		// declare an array
		int arr[]= {12,22,45,25,32}; //sequence no. of series stored in memory
		//using for each loop to print array
		for (int i:arr) { // array index is represented by i 
			System.out.println(i);
		}
	}

}
