package ControlStatement;

public class WhileLoop {
	public static void main(String[] args) {	
		int i=1;
		while(i<=10) { // condition at last
			System.out.println(i);
		i++;	 // increment each after i value till 10 
		// increment after print because if the increment happens first then the value will be printed first 
		}
	}
}
