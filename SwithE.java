package ControlStatement;

public class SwithE {
public static void main(String[] args) {
	char ch='a';
	switch (ch) {
	case 'd' :
		System.out.println("first case execute");
		break;
	case 'c' :
		System.out.println("second case execute");
		break;
	case 'a':
		System.out.println("third case execute");
		break;
	case 'h' :
		System.out.println("foutrh case execute");
		break;
	default: System.out.println("----Invalid----");
}
}
}